CREATE DATABASE biblioteca;

CREATE TABLE AUTORES (
ID SERIAL PRIMARY KEY,
NOME VARCHAR(255) NOT NULL,  
IDADE INTEGER(3),
);

CREATE TABLE LIVROS (
ID SERIAL PRIMARY KEY,
NOME VARCHAR(255) NOT NULL,
GENERO VARCHAR(50),
EDITORA VARCHAR(50),
DATA_PUBLI DATE NOT NULL,
ID_AUTOR INT REFERENCES AUTORES (ID)
);
